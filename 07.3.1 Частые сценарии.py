""" Сигнальные метки """
# Используются, когда надо чтобы одна часть программы узнала, о происходящем в другой части программы

# программа определяющая, что натуральное число является ПРОСТЫМ (не имеет делителей, кроме 1 и самого себя)
num = int(input('Введите число больше нуля: '))
flag = True  # взводим сигнальный флажок

for i in range(2, num):  # начинаем с 2-ки, что бы исключить деление на 1
    if num % i == 0:  # если исходное число делится на какое-либо отличное от 1 и самого себя
        print(i, num % i)
        flag = False

if num == 1:
    print('Это единица, она не простая и не составная')
# elif flag == True: ( понятное, но некорректное написание (аналогия: масло масленное))
elif flag:
    print('Число простое')
else:
    print('Число составное')

""" Максимум и минимум """
# Поиск наибольшего или наименьшего значения в некоторой последовательности чисел
# Программа считывает 5 положительных чисел и находит среди них наибольшее число
largest = -1
# . В качестве начального значения взято число -1,
# поскольку мы знаем, что все числа положительны, таким образом уже первое сравнение приведет к переприсваиванию
for i in range(5):
    num = int(input())
    if num > largest:
        largest = num
print('Наибольшее число равно', largest)

# Другой вариант, когда в качестве начального значения переменной, сразу принимается первый элемент последовательности
# Программу, которая считывает 5 чисел (необязательно положительных) и находит среди них наибольшее:
largest = int(input())  # принимаем первое число за максимальное
for i in range(4):
    num = int(input())
    if num > largest:
        largest = num
print('Наибольшее число равно', largest)

# аналогично для нахождения наименьшего числа
smallest = int(input())  # принимаем первое число за максимальное
for i in range(4):
    num = int(input())
    if num < smallest:
        smallest = num
print('Наименьшее число равно', smallest)

