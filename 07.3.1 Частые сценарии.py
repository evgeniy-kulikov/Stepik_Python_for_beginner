""" Подсчет количества """
# программа, которая считывает 5 чисел и определяет сколько из них больше 10
counter = 0
for i in range(5):
    num = int(input())  # 5 раз получаем значение  < num >
    if num > 10:
        counter += 1  # когда мы считываем число большее 10, мы добавляем 1 к нашему счетчику < counter >
print('Было введено', counter, 'чисел, больших 10.')

# так же посчитаем еще и количество значений равных нулю среди введенных чисел
# Используется два счетчика
counter1 = 0
counter2 = 0
for i in range(5):
    num = int(input())
    if num > 10:
        counter1 += 1
    if num == 0:
        counter2 += 1
print('Было введено', counter1, 'чисел, больших 10')
print('Было введено', counter2, 'нулей')

# подсчитать количество чисел из диапазона [1;100], квадрат которых оканчивается на 4
counter = 0
for i in range(1, 101):
    if i**2 % 10 == 4:
        counter += 1
print(counter)

""" Вычисление суммы и произведения """
# программа, которая считывает 5 чисел и определяет сумму тех из них, которые больше 10
total = 0
for i in range(5):
    num = int(input())
    if num > 10:
        total = total + num
print('Сумма чисел больших 10 равна',  total)

