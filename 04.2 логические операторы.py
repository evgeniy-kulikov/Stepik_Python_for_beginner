# Даны две различные клетки шахматной доски. Напишите программу,  которая определяет,
# может ли король попасть с первой клетки на вторую одним ходом.
# Программа получает на вход четыре числа от 1 до 8 каждое,
# задающие номер столбца и номер строки сначала для первой клетки, потом для второй клетки.
# Программа должна вывести «YES», если из первой клетки ходом короля можно попасть во вторую, или «NO» если не может
# input: На вход программе подаётся четыре числа от 1 до 8
# output: Программа должна вывести «YES» или «NO» в соответствии с условием задачи
column1, line1, column2, line2 = int(input()), int(input()), int(input()), int(input())
if ((column1 - 1) <= column2 <= (column1 + 1)) and ((line1 - 1) <= line2 <= (line1 + 1)):
    print('YES')
else:
    print('NO')

