# 4.2 Логические операции
#
# and — логическое умножение
# or — логическое сложение
# not — логическое отрицание
#
#   *   *   *   *   *   *   *   *   *   *   *   *   *   *   *   *
#
# Оператор and
#   таблица истинности для оператора and
#   a	    b	    a and b
#   False	False	False
#   False	True	False
#   True	False	False
#   True	True	True
# Разрешить вход для учеников от двенадцати лет, которые учатся по крайней мере в 7 классе.
# Доступ к ней тем, кто младше, надо запретить
