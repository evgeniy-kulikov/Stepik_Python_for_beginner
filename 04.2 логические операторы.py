#
# В этом ветвлении блок кода выполняется только при выполнении обоих условий одновременно!
#
# Напишите программу, которая проверяет, что все три цифры натурального трёхзначного числа различны.
num = int(input())
d3 = num % 10
d2 = num % 100 // 10
d1 = num // 100
if d3 != d2 and d3 != d1 and d2 != d1:
    print('Цифры различны')
else:
    print('Цифры не различны')
# В этом ветвлении блок кода выполняется только при выполнении трех условий одновременно!
#
#   *   *   *   *   *   *   *   *   *   *   *   *   *   *   *   *
#
# Оператор or
# Оператор or также применяется для объединения условий.
# Однако, в отличие от and, для выполнения блока кода достаточно выполнения хотя бы одного из условий
# таблица истинности для оператора or
#   a	    b	    a or b
#   False	False	False
#   False	True	True
#   True	False	True
#   True	True	True
city = input('В каком городе вы живете?: ')
if city == 'Москва' or city == 'Санкт-Петербург' or city == 'Екатеринбург':
    print('Доступ разрешен.')
else:
    print('Доступ запрещен.')
#
#   *   *   *   *   *   *   *   *   *   *   *   *   *   *   *   *
#
# Оператор not позволяет инвертировать (т.е. заменить на противоположный) результат логического выражения
# Таблица истинности для оператора not:
#   a	    not a
#   False	True
#   True	False
# следующий код:
age = int(input('Сколько вам лет?: '))
if not (age < 12):
    print('Доступ разрешен.')
else:
    print('Доступ запрещен.')
