# n школьников делят k мандаринов поровну, неделящийся остаток остается в корзине.
# Сколько целых мандаринов достанется каждому школьнику? Сколько целых мандаринов останется в корзине?
# input: количество школьников и количество мандаринов.
# output: количество школьников и количество мандаринов которое останется в корзине.
n = int(input('школьников: '))
k = int(input('мандаринов: '))
c = k // n
b = k - (c * n)
print(c)
print(b)

