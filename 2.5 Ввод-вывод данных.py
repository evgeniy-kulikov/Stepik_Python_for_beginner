h = m//60
m_residue = m - h * 60
print(f'{m} мин - это {h} час {m_residue} минут.')

# Напишите программу, в которой рассчитывается сумма и произведение цифр положительного трёхзначного числа.
# input: подаётся положительное трёхзначное число
# output: вывести два числа с поясняющим текстом: сумма цифр и произведение цифр
n = int(input('трёхзначное число: '))
digit1 = n // 100
digit2 = (n // 10) % 10
digit3 = n % 10
summa = digit1 + digit2 + digit3
multi = digit1 * digit2 * digit3
print(f'Сумма цифр = {summa}\nПроизведение цифр = {multi}')

