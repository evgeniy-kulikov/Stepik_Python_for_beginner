# Задача 10:
# Назовем число интересным, если в нем разность максимальной и минимальной цифры равняется средней по величине цифре.
# Напишите программу, которая определяет интересное число или нет. Если число интересное, следует вывести –
# «Число интересное» иначе «Число неинтересное».
# input: На вход программе подается целое трехзначное число.
# output: Программа должна вывести текст в соответствии с условием задачи.
n = int(input(''))
n_1 = n // 100
n_2 = (n % 100) // 10
n_3 = n % 10
n_min = min(n_1, n_2, n_3)
n_max = max(n_1, n_2, n_3)
n_sum = n_1 + n_2 + n_3
n_mid = n_sum - n_min - n_max
if n_mid == n_max - n_min:
    print('Число интересное')
else:
    print('Число неинтересное')

# Другое мое решение
n = int(input(''))
n_1 = n // 100
n_2 = (n % 100) // 10
n_3 = n % 10
s = [n_1, n_2, n_3]
s.sort()
if s[1] == s[2] - s[0]:
    print('Число интересное')
else:
    print('Число неинтересное')

# Другое решение. Интересный путь наполнения списка!
n = input('')
s = [int(i) for i in n]
s.sort()
if s[1] == max(s) - min(s):
    print('Число интересное')
else:
    print('Число неинтересное')

# Другое решение
# при красивом 3-x значном числе максимальная цифра равна сумме двух оставшихся
