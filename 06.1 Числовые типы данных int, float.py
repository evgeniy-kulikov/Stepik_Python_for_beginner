# Задача 5:
# На вход программе подается число nn – количество собачьих лет.
# Напишите программу, которая вычисляет возраст собаки в человеческих годах.
# Примечание: В течение первых двух лет собачий год равен 10.5 человеческим годам.
# После этого каждый год собаки равен 4 человеческим годам.
# input: На вход программе подаётся натуральное число – количество собачьих лет.
# output: Программа должна вывести возраст собаки в человеческих годах.
dog_age = int(input(''))
if dog_age < 2:
    man_age = dog_age * 10.5
elif dog_age == 2:
    man_age = int(dog_age * 10.5)
else:
    man_age = 21 + ((dog_age - 2) * 4)
print(man_age)

