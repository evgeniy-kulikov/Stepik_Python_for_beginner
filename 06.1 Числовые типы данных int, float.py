# Задача 12:
# На плоскости манхэттенское расстояние между двумя точками (x1; y1) и  (x2; y2) пределяется так:
# |x1 - x2| + |y1 - y2|
# Напишите программу определяющую манхэттенское расстояние между двумя точками, координаты которых заданы.
# input: На вход программе подается четыре целых числа, каждое на отдельной строке
# output: Программа должна вывести одно число – манхэттенское расстояние.
x1, y1, x2, y2 = int(input('')), int(input('')), int(input('')), int(input(''))
way = abs(x1 - x2) + abs(y1 - y2)
print(way)

